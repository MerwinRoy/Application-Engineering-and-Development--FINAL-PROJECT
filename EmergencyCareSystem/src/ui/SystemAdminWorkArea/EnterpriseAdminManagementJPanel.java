    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        Enterprise enterprise = (Enterprise) cmbEnterprise.getSelectedItem();
        
        String username = txtUserName.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String name = txtName.getText();
        if( txtUserName.getText().trim().isEmpty() ||
            txtName.getText().trim().isEmpty() )
        {
            JOptionPane.showMessageDialog(null, "Please enter all the details");
            return;
        }
        
        Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);
        
        UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
        populateTable();
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        
        //container removed
        container.remove(this);
        
        //component array to collect container components
        Component[] compArray = container.getComponents();
        
        //component object to refer to array
        Component component = compArray
                [compArray.length - 1];
        
        //New JPanel
        SystemAdminWorkAreaJPanel sysAdminWorkPanel = (SystemAdminWorkAreaJPanel) component;
        
        //populate tree on JPanel
        sysAdminWorkPanel.
                populateTree();
        
        //New CardLayout
        CardLayout cardLayout = (CardLayout) container.
                getLayout();
        
        //previous container loaded
        cardLayout.previous(container);
    }//GEN-LAST:event_btnBackActionPerformed

