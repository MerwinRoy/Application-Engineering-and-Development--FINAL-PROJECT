        private void populateEnterpriseComboBox(Network network)
    
    {
        //remove items from combobox
        cmbEnterprise.
                removeAllItems();
        
        //for loop to iterate through list of enterprise
        for (Enterprise ep : network.
                getEnterpriseDirectory().
                getListOfEnterprise())
        {
                cmbEnterprise.addItem(ep);
        }
        
    }
    


    private void populateNetworkComboBox()
    {
        
        cmbNetwork.
                removeAllItems();
        
        for (Network network : ecoSystem.
                getNetworkList()){
            cmbNetwork.
                    addItem(network);
        }
    }
    

    
        //function to populate table
    private void populateTable() 
    
    {
        //tableModel object created
        DefaultTableModel tableModel = (DefaultTableModel) tblEnterprise.
                getModel();

        //set row count to 0
        tableModel.setRowCount(0);
        
        //for loop to iterate through list of networks
        for (Network network : ecoSystem.
                getNetworkList()) 
        {
            for (Enterprise enterprise : network.
                    getEnterpriseDirectory().
                    getListOfEnterprise()) 
            {
                
                for (UserAccount userAccount : enterprise.
                        getUserAccountDirectory().
                        getListOfUserAccount()) 
                
                {
                    Object[] row = new Object[3];
                    
                    row[0] = enterprise.
                            getName();
                    
                    row[1] = network.
                            getName();
                    
                    row[2] = userAccount.
                            getProfileName();

                    tableModel.addRow(row);
                }
            }
        }
    }
    
