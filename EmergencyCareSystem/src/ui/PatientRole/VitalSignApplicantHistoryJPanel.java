    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblVitalSignsHistory.getSelectedRow();

        if (selectedrow>=0)
        {
            VitalSign vitalSign=(VitalSign)tblVitalSignsHistory.getValueAt(selectedrow, 0);
            
            
            txtHeartRate.
                    setText(String.
                            valueOf(vitalSign.
                    getHeartRate()));
            
            txtBloodPressure.
                    
                    setText(String.
                            valueOf(vitalSign.
                    getBloodPressure()));
            txtPulse.
                    setText(String.
                            valueOf(vitalSign.
                    getPulse()));
            txtWeight.
                    setText(String.
                            valueOf(vitalSign.getWeight()));
        }
        else
        {
            JOptionPane.
                    showMessageDialog(null,"Please select any row");
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed


