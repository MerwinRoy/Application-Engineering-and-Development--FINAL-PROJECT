    public void populateTable()
    {
        DefaultTableModel tableModel =(DefaultTableModel) tblVitalSignsHistory.getModel();
        tableModel.setRowCount(0);
        for(WorkRequest workRequest : account.
                getWorkQueue().
                getListOfWorkRequest())
        {
            if(!(workRequest instanceof LabWorkReq)
                    && !(workRequest instanceof DonorWorkReq) 
                    && !(workRequest instanceof ManagementWorkReq))
            {
        dtwr = (DocWorkReq)workRequest;
        
        
        
        for (VitalSign vitalSign : dtwr.getVitalSignList())
        {
            Object row[]= new Object[2];
            row[0]=vitalSign;
            row[1]=vitalSign.
                    getTimeStamp();
            tableModel.addRow(row);
        }
        
    }}}
    
