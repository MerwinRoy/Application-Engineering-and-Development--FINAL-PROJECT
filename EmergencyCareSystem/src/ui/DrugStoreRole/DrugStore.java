    public void populateTable()
    
    {
        //table model object created
        DefaultTableModel tableModel = (DefaultTableModel)tblWorkRequest.
                getModel();
        
        //set row count of table model to 0
        tableModel.
                setRowCount(0);
        
        //for loop to iterate through list of work requests
        for(WorkRequest request : org.
                getWorkQueue().
                getListOfWorkRequest()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.
                    getSenderAccount().
                    getEmployee().
                    getName();
            row[2] = request.
                    getReceiverAccount() == null ? null : request.
                            getReceiverAccount().
                            getEmployee().
                            getName();
            row[3] = request.
                    getStatus();
            
            //Add row after every iteration to table model
            tableModel.
                    addRow(row);
        }
    }

