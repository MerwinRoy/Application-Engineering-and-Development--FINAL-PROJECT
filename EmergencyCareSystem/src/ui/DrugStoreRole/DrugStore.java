    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed

        //selected row from table
        int selectedRow = tblWorkRequest.
                getSelectedRow();

        if (selectedRow < 0)
        {
            return;
        }

        //WorkRequest object
        WorkRequest workRequest = (WorkRequest)tblWorkRequest.
                getValueAt(selectedRow, 0);
        
        //set receiver account
        workRequest.
                setReceiverAccount(account);
        
        //set status
        workRequest.
                setStatus("Pending");
        
        //refresh table
        populateTable();
    }//GEN-LAST:event_btnAssignActionPerformed

    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed

        //selected row from table
        int selectedRow = tblWorkRequest.
                getSelectedRow();

        if (selectedRow < 0)
        {
            return;
        }

        //get drugstore work request object value from table
        DrugStoreWorkReq drugStoreWorkRequest = (DrugStoreWorkReq)tblWorkRequest.
                getValueAt(selectedRow, 0);

        //set status
        drugStoreWorkRequest.
                setStatus("Processing");

        //New JPanel object
        ProcessWorkRequestJPanel pwrJPanel = new ProcessWorkRequestJPanel(container, drugStoreWorkRequest);
        
        //add container
        container.
                add("processWorkRequestJPanel", pwrJPanel);
        
        //CardLayout object creation
        CardLayout cardLayout = (CardLayout) container.
                getLayout();
        
        cardLayout.
                next(container);
    }//GEN-LAST:event_btnProcessActionPerformed


